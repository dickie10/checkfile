<h3 class="news">OSGeo recognizes Effort in Big Geospatial Analytics </h3><p><b>

Being selected by the Open Source Geospatial Foundation (OSGeo) to become part of their “OSGeo Live” demonstration DVD dignifies a project as being particularly topical, valuable, high-quality, and innovative. The rasdaman geo analytics engine has been added to OSGeo Live 5.5 as the only geo raster server capable of handling unlimited volumes of multi-dimensional “Big Data”.

</b></p><p>

OSGeo Live is a self-contained bootable DVD, USB thumb drive or Virtual Machine based on Xubuntu that allows to try a wide variety of open source geospatial software without install¬ing anything. It is composed entirely of free software, allowing it to be freely distributed, duplicated and passed around. OSGeo Live can be downloaded from live.osgeo.org and several mirror sites. 
“This rewarding acknowledgement of rasdaman in the geospatial Big Data world underlines the importance of going the next technology step, beyond mere map serving; geo analytics is indispensable in today’s flow of geospatial information”, comments Peter Baumann, principal architect of rasdaman and well-known editor of OGC raster service standards. 
In parallel, rasdaman has been given “incubation” status in OSGeo’s open-source geospatial software branding program. Being acknowledged by OSGeo means that not only the software itself, but the project overall has been thoroughly examined in all aspects. Criteria considered include geo relevance, code quality, documentation, strictly open-source code provenance, community, and project management. Every project gets assigned a voluntary mentor; for rasdaman, Bruce Bannerman from the Australian Bureau of Meteorology has kindly accepted this task. 

