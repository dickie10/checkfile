<h3 class="news">rasdaman 8.4. released </h3><p><b>

April 2013. Version 8.4 of the rasdaman Array Analytics Engine [1] has been released.
Concurrently, this version is made available for the open-source variant, rasdaman community [2], and the proprietary enhancement, rasdaman enterprise [3].

</b></p><p>

The international team was breathtakingly busy adding features and closing tickets.
Among the new features is a classification operator suitable for elevation coloring, but also for content-based image analysis.
Maybe most important, the whole release process has been scrutinized and the trac wiki has been opened to the public for writing.
For the community this means they have more overview and improved ways of impacting direction of development.

</p><p>

"By introducing semantic versioning we now can provide stable releases better while, at the same time, allowing developers to collaborate on new features"
says Dr Beccati, senior software developer from Ferrara / Italy who has introduced these new features into the release management process.

</b></p><p>

Meantime development continues. This summer, rasdaman 9.0 will be released bringing along substantially enhanced functionality and novel parallelization features.

</p><p>

The rasdaman technology has coined the technology of Array Databases [4] and still today is the most advanced and operational system.
It is reference implementation of the OGC Web Coverage Service (WCS) Core and Web Coverage Processing Service (WCPS).
In a unique way rasdaman combines performance, scalability, flexibility, and functional power.

</b></p><p>

Rasdaman community is available for download as source code, installable RPM packages, and as preconfectioned Virtual Machine.
For ways to obtain rasdaman enterprise, see the company website [3].

</b></p>
