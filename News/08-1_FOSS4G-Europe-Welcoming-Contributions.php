<h3 class="news">FOSS4G-Europe Welcoming Contributions </h3><p><b>
Independent software developers, scientists, industry experts, and agency representatives will come to meet at the FOSS4-Europe conference held in Bremen, Germany from July 15 to 17. Contributions can now be submitted on the conference website <a href="http://ea.sendcpt.com/_la.php?&nid=1681693&sid=196282272&lid=6426528&enc=687474703a2f2f7777772e666f737334672d652e6f7267&tg=">http://www.foss4g-e.org/</a> until April 15.
</b></p><p>
Under this year’s motto “Independent Innovation for INSPIRE, Big Data and citizen participation” the conference series FOSS4G-Europe (Free and Open Source Software for Geospatial - Europe) is uniting the GIS community from a broad spectrum of fields like geodesy, geo information, land management, and remote sensing. Industry and public authorities are expressly invited as they increasingly realize value and economic potential of free and open source products.
</p><p>
Several additional events make the FOSS4G-Europe in Bremen a unique event. A public viewing of the soccer World Championship final will kick off a week packed with events. The week will open with a workshop “Big Geo Data & INSPIRE” especially targeted at public authorities. INSPIRE Annex II and III will be discussed in relation to the pertaining standards of the Open Geospatial Consortium <a href="http://ea.sendcpt.com/_la.php?&nid=1681693&sid=196282272&lid=6426533&enc=687474703a2f2f7777772e6f70656e67656f7370617469616c2e6f7267&tg=">http://www.opengeospatial.org/</a>. Canadian Jeff McKenna, President of the OSGEO Foundation, will open the event with his keynote talk. As a charter member McKenna has got involved with spreading and usage of open-source software for geo information systems for many years. In a plenary, the winners of the international academic NASA World Wind
Challenge Europe programming contest will be announced and honored at the conference. 
</p><p>
Being in the tradition of the worldwide FOSS4G conference series, this event will bring together open-source experts and enthusiasts from all over Europe and beyond creating a unique, visionary atmosphere.
</p><p>
Altogether, this unique technology demonstration paves the way for services merging data across data centers, nations, continents - ultimately, across the planet.
