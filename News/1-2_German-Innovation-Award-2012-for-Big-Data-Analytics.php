<h3 class="news">German Innovation Award 2012 for Big Data Analytics </h3><p><b>

The research spin-off, rasdaman GmbH, has been awarded the INNOVATIONSPREIS-IT (Innovation Award IT) 2012 in the category “BEST OF 2012 Open Source”. The national jury thus acknowledged the world technology lead of the high-performance raster analytics engine, rasdaman (“raster data manager”), selected from over 2,500 applications.

</b></p><p>

The rasdaman technology enables fast and flexible access to multi-dimensional sensor, image, and statistics data of unlimited volume; examples include 1D time series, 2D satellite images, 3D x/y/t image timeseries and x/y/z geophysics data, as well as 4D climate data. Being domain independent, rasdaman equally well supports application domains in Earth, Space, and Life sciences, to name but a few.
Among the distinguishing features is the capability of answering ad-hoc questions to perform filtering the desired data from large-scale archives and to process the extracted data into the shape users need them. This includes fusion of data coming from different locations and having different resolution, extent, and structure.
The enabling idea is to combine standard database query languages with image processing languages, resulting in a novel, powerful means to access massive archives over the Web. Meantime this principle has found its way into international standards – the Open Geospatial Consortium (OGC) has crafted its raster query language, WCPS, along the rasdaman concepts. While rasdaman has been developed over 17 years into a stable, mature technology, international efforts have emerged with similar goals, most remarkably the US SciDB initiative.

Under the patronage of German national commissioner for information technology, Cornelia Rogall-Grothe, and IBM Germany the INNOVATIONSPREIS-IT is in its ninth year of scouting IT innovations.

