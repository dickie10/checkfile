<h3 class="news">OGC Adopts Enhanced Spatio-Temporal Coverage Service </h3><p><b>

The Open Geospatial Consortium (OGC) Web Coverage Service (WCS) Standard establishes an open, interoperable service interface for accessing, filtering, and processing of multi-dimensional spatio-temporal coverages, such as sensor time series, satellite imagery and image time series, and climate data. Following the complete overhaul of WCS in 2010, leading to version 2.0, now an enhanced corrigendum version 2.0.1 has been produced.

</b></p><p>

En detail, WCS actually comprises a modular suite of standards based on the common WCS Core 2.0.1 becoming available now. The GML Application Schema for Coverages defines OGC’s common coverage model, allowing coverages to freely be exchanged between services implementing different standards such as GML, SWE, WCS, and WPS. Further service extensions include the Web Coverage Processing Service (WCPS) standard which establishes a multi-dimensional raster query language. The Earth Observation Application Profile (EO-WCS) is a candidate standard which specializes WCS for the satellite imagery domain.
“The new version satisfies stakeholder requests received and offers significantly improved functionality, flexibility, and usability”, says Peter Baumann, chair of the WCS Standards Working Group and editor of the specifications. Within the next few days, OGC will make new specification available on the OGC website where it can be found at www.opengeospatial.org/standards/wcs. 
The OGC is an international consortium of more than 435 companies, government agencies, research organizations, and universities participating in a consensus process to develop publicly available geospatial standards. OGC Standards support interoperable solutions that "geo-enable" the Web, wireless and location-based services, and mainstream IT. OGC Standards empower technology developers to make geospatial information and services accessible and useful with any application that needs to be geospatially enabled. Visit the OGC website at www.opengeospatial.org.

