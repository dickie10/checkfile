2015-oct-23: The 1h TV documentation <a href="http://ea.newscpt4.de/_la.php?&nid=2469149&sid=196282272&lid=10030894&enc=68747470733a2f2f7777772e796f75747562652e636f6d&tg=watch?v=ldsTcAvXwSc">Big Earth Data - the digitized planet</a> produced by Jacobs University Bremen and rasdaman GmbH as part of the EarthServer project in cooperation with the german broadcast ZDF/ARTE is in 2nd place of the "<a href="http://ea.newscpt4.de/_la.php?&nid=2469149&sid=196282272&lid=10030899&enc=687474703a2f2f7777772e676f657468652e6465&tg=ins/th/prj/wif/flm/en14471923.htm">International Scientific Film Festival</a> in Szolnok, Hungary. The documentary which has been funded by the European Commission and broadcasted in February 2015 is concerned with the chances and challenges dealing with Big Earth Data.
