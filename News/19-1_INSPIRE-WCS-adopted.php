<h3> INSPIRE WCS adopted </h3>
<p><b>
December 2016 - Last month INSPIRE officially adopted the OGC Web 
Coverage Service (WCS) as INSPIRE Coverage Download Service. Jordi 
Escriu, Facilitator of the corresponding Thematic Cluster 3, calls "WCS 
the natural way to deliver and exploit coverage data".
</b></p>
<p>
The OGC Web Coverage Service (WCS) standards suite defines services on
coverages, a concept including spatio-temporal sensor, images
(timeseries), simulation, and statistics data. In practice, coverages
often represent "Big Data" as orthoimagery, elevation data, weather
forecasts, and point clouds. The WCS Core establishes basic
functionality all implementations must provide: access to whole
coverages or parts thereof via spatial/temporal subsetting. Extensions
add optional functionality, such as data format encodings (like GeoTIFF,
 JPEG2000, NetCDF), service model extensions (such as scaling and
reprojection), and protocol bindings (GET, POST, SOAP, and in future
REST).
</p> <p>
Representatives of industry, administration, and research are positive
about the standard model WCS and its reference implementation rasdaman:
"The RASDAMAN product is currently the world leading environment in this
 domain and the standard working horse for OGC standardisation on these
innovative data access interfaces", Guenter Landgraf from European Space
 Agency (ESA) said.
<p> </p>
Already during the workshop “Implementation and potential of INSPIRE
coverage data and WCS” at the INSPIRE Conference in Barcelona last Fall
attendees were impressed by the options and possibilities WCS is
offering. In his presentation INSPIRE delegate, Peter Baumann, and Alex
Dumitru from Jacobs University showed the ease of using WCS in a
hands-on-session doing live ingest and retrieval using rasdaman.
Presenter Julia Wagemann from the European Centre for Medium-Range
Weather Forecasts (ECMWF) underlined: “Experience from ECMWF on
coverages and WCS is a good example of the benefits and the potential of
 such kind of data and services. Worth to look at and take into account
by other organizations and communities." Editor of the INSPIRE WCS
specification is James Passmore from British Geological Suervey (BGS)
who has gained ample experience in coverage services as
participant of the <a href="http://www.earthserver.eu">EarthServer</a>
 project. Peter Baumann, head of rasdaman, was involved as national
German delegate to INSPIRE; based on a long-standing experience in
building flexible, heavy-lifting services he is editor of "Big Datacube"
 standards in OGC and ISO.
</p><p>
