<h3> rasdaman joining EARSC </h3><p><b>
January 2017 - Starting 2017, rasdaman is full member of the European 
Association of Remote Sensing Companies (EARSC), a not-for-profit 
association promoting Earth Observation (EO) technology especially from 
European companies. Monica Miguel-Lago expressed that EARSC "are 
delighted to accept your company as new EARSC full Member".
</b></p>
<p>
With these activities EARCS fosters development of the European EO
geo-information services industry. EARSC is open to full and observer
members with an interest in using or providing services delivering
information based on observations of the Earth and its environment from
any platform; satellite, aircraft or UAV.
</p><p>
"We see high value in joining EARSC and the hitech portfolio aggregated
there, it's like meeting with friends", says Peter Baumann, rasdaman
CEO. As a key technology provider in Big Earth Data services, rasdaman
enables researchers to browse, access, and analyze massive
multi-dimensional data sets from a wide range of source. rasdaman is
deeply involved in several Earth observation projects, such as the <a href="http://ea.newscpt.com/_la.php?&amp;nid=3035035&amp;sid=196282272&amp;lid=12733300&amp;enc=687474703a2f2f7777772e65617274687365727665722e6575&amp;tg=">EarthServer</a>
 initiative, where monitoring the Earth and analyzing EO timeseries data
 are at the heart. The rasdaman technology allows real-time analytics on
 3D and 4D Petabyte cubes for spatio-temporal sensor, image, simulation,
 and statistics data as well as their intercontinental fusion. This
power of data handling can be wrapped into
any kind of clients, ranging from direct visual interaction through maps
 and virtual globes (like OpenLayers and NASA/ESA WebWorldWind) over Web
 GIS (like QGIS) to high-end analytisc (such as python).
