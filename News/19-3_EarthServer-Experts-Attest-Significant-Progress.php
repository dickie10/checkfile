<h3> EarthServer: Experts Attest Significant Progress </h3>
<p><b> Brussels, January 2017 - "The <a href="http://ea.newscpt.com/_la.php?&amp;nid=3035035&amp;sid=196282272&amp;lid=12733300&amp;enc=687474703a2f2f7777772e65617274687365727665722e6575&amp;tg=">EarthServer-2</a>
 project has successfully completed its second milestone and has made 
significant progress towards the eventual aim of producing a 
comprehensive big data analytics platform for Earth Science", the 
European Commission currently attested on the occasion of projectÂ´s 
midterm review performed in Brussels.
</b></p>
<p>
Among the main innovation activities, rasdaman, the platform technology
of all services, has been extended with new functionalities, such as
support for GRIB data and NetCDF ingestion. A new metadata engine,
FEMME, has been developed and, over the next period, will be fully
integrated with xWCPS, which leads to combined array data and metadata
retrieval. Search facilities on both XML-encoded metadata, freetext, and
 OGC coverages will be an important step as well. Work on mixed queries
enabling new user interfaces has been done already, and will be
contined. Services for marine, climate, earth observation, and planetary
 data have been created using the NASA/ESA WebWorldWind visualisation
API. Live demonstrations of pilots during the review meeting showed that
 significant progress has been made in producing high-quality
interfaces, as well as in ingesting valuable data resources
and integrating new providers, such as ANI.
</p><p>
One of the important results achieved is a dramatic reduction of the
amount of data downloaded. While the extraction of data with traditional
 methods requires users to download all data for the time series, up to
Terabytes, with rasdaman users can access data with surgical accuracy,
thereby reducing data transfer to Kilobytes. Additionally, access is
simplified substantially; for example, users only need to click on the
globe for retrieving a timeseries values for this point.
</p><p>
Further, the project has achieved important impact on international big
data standardization process. For example, INSPIRE has chosen rasdaman
as reference implementation for the INSPIRE WCS Download Service. The
technical capability gained during the project, enabled the
participating SMEs to substantially strengthen their market footprint.
</p><p>
In the next period, the Petabyte frontier will be attacked, further
enhancements will be implemented on service as well as technology level,
 and "Big Datacube" standardization will be progressed further.
</p><p>
Contact: Dr. Peter Baumann
</p>
