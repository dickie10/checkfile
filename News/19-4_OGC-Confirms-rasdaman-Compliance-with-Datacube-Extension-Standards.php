<h3> OGC Confirms rasdaman Compliance with Datacube Extension Standards </h3><p><b>
Mayland/USA, January 2017 - OGC officially confirms that rasdaman has 
achieved compliance certification on a number of important extensions to
 the OGC "Big Datacube" standard, Web Coverage Service (WCS).
</b></p>
<p>
OGC operates a compliance program which allows implementations of
standards to be tested free of cost test through a neutral, verified,
suite maintained by OGC (see <a href="http://ea.newscpt.com/_la.php?&amp;nid=3035035&amp;sid=196282272&amp;lid=12733310&amp;enc=687474703a2f2f7777772e6f70656e67656f7370617469616c2e6f7267&amp
;tg=compliance">www.opengeospatial compliance</a>).
 The purpose is to increase system interoperability while reducing
technology risks. Buyers gain confidence that a compliant product will
work with another compliant product based on the same OGC standard,
regardless of which company developed the product. The rasdaman array
analytics engine has passed the WCS test suite for Core and GET/KVP
extension already in 2013 and today is official reference implementation
 in OGC and also INSPIRE. While rasdaman always has been a first
implementer of the OGC Big Data standards - and
currently offers the most comprehensive support for the WCS suite - now
there is official confirmation for the extensions WCS Post, WCS
Processing, WCS Scaling, WCS Interpolation, WCS Range Subsetting.
By combining the complementary know-how of both partners Big Picture
aims at establishing large-scale automated analysis offering individual
insights and support for agricultural production in Germany.
</p>
<p>
These compliance tests have been developed by Fuzhou University in
China. "We are very happy about Fuzhou University undertaking the work
of establishing such a test suite", says Peter Baumann, WCS suite editor
 and rasdaman Principal Architect. "This valuable service to the
community allows tool providers like us to get an independent compliance
 confirmation, which of course is worth more than any self assessment".
</p>
<p>
Work on demonstrating compliance is ongoing: testing rasdaman against
the WCS CRS extension is already in an advanced state, and the WCS-T
extension is planned to get addressed next.
</p>
