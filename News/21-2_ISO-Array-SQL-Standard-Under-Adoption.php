<h3>ISO Array SQL Standard Under Adoption</h3>
<p><b>Okayama, summer 2017 - ISO Array SQL, the SQL "datacue" extension, has been finalized and "frozen" in its design at the recent ISO meeting in Okayama, Japan. Following the planned timeline, at the next voting stage only minor textual changes will be considered any more, allowing to complete the new standard still within this year.</b>
</p><p>
"Array SQL" is a common term for ISO 9075 SQL Part 15: MDA (Multi-Dimensional Arrays). This candidate standard represents an extension to the standard database language with multi-dimensional arrays. Currently in the final stage of adoption, SQL/MDA will be a game changer according to international experts in that it enables SQL to be used in environments where its gaps have prevented use so far, such as Earth, Space, and Life sciences.
The SQL/MDA specification has been proposed and elaborated by the rasdaman team, following internal scrutiny by ISO. Early on, an industry member of the SQL group - investigating on rasdaman and competing approaches like SciDB and SciQL - concluded that rasdaman was the superior data model and believed that "rasdaman is the only one of the three data models to come to market". Through rasdaman, the concepts are practice-proven with 1000-fold parallelization and on Petascale operational installations.
</p><p>
Following intensive work for achieving a seamless integration with the SQL standard, SQL/MDA will next be voted on by the national delegations as a "Draft International Standard" starting this August, at which stage only editorial changes will be possible any more. Therefore, concepts and details are stable now. According to plan, final adoption is planned for the end of 2017.
</p><p>
A publicly accessible summary of the standard is available, e.g., in: D. Misev, P. Baumann: Enhancing Science Support in SQL. Proc. Workshop Data and Computational Science Technologies for Earth Science Research (co-located with IEEE Big Data), Santa Clara, US, October 29, 2015.
</p><p>
