<h3>EarthServer: 1+ Petabyte Analysis-Ready Datacubes</h3>
<p><b>
 November 2017 - EarthServer, the European Union Big Data initiative, has reached the next milestone: Today in the second phase extending through April 2018, the Italian data provider MEEO S.R.L. exceeds 1 Petabyte of spatio-temporal, analysis ready data processed through its operational Earth Observation Data Service. 
</b>
</p><p>
 The intercontinental EarthServer initiative unites Europe, US, and Australia in the quest for scalable datacubes on 3D x/y/t satellite image timeseries and 4D x/y/z/t weather data, based on the European datacube technology, rasdaman. After 6 years project running, a particularly spectacular milestone has been reached: operational real-time data analytics on more than a Petabyte of satellite timeseries through open OGC standard interfaces.
</p><p>
<a href="http://ea.newscpt4.de/_la.php?&nid=3412585&sid=196282272&lid=14453825&enc=68747470733a2f2f656f64617461736572766963652e6f7267&tg=">EO Data Service</a> has crossed the 1 PB data providing access and exploitation capabilities to the Copernicus Sentinel-2A (launched on 23 June 2015) and Sentinel-2B (launched on 7 March 2017) satellites. The Sentinel 2 archives grow at a rate of 3TB/day and allow the Earth Observation scientists in monitoring variability in land surface conditions", says Simone Mantovani, CEO of MEEO S.R.L.
</p><p>
With the European partners funded by the European Commission, the EarthServer initiative was launched in 2011 to establish a standards-based, highly scalable framework for providing analysis-ready Earth science data. The underlying distinguishing principle is a geospatial datacube analytics language, the OGC Web Coverage Processing Service (WCPS), and scaling its use into the Petascale.
</p><p>
"It was planned like this, but still it excites me to see the Petabyte frontier finally crossed. Building such a datacube allows for new science and applications using both new and old data‚Äù, comments EarthServer coordinator, Prof. Dr. Angelo Pio Rossi from Jacobs University.
</p><p>
"And the next challenge is already being tackled", adds Prof. Dr. Peter Baumann, rasdaman architect and Technical Coordinator of EarthServer. "Now we are working closely with the European Centre for Medium Range Weather Forecast (ECMWF) to unleash their 220 PB archive via rasdaman and OGC datacube standards". 
</p>
