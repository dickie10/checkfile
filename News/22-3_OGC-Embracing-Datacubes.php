<h3>OGC Embracing Datacubes</h3>
<p><b>
 November 2017 - Datacubes are receiving attention worldwide as a novel concept making raster data more accessible and easier to analyse. Based on the coverage standards suite of OGC, ISO and INSPIRE
</b>
</p><p>
 Datacubes present massive spatio-temporal raster data as multi-dimensional cubes, such as x/y/t image timeseries and x/y/z/t weather data. Among others, Petascale timeseries analysis can become simple and extremely fast, as the EarthServer initiative shows.
</p><p>
August this year a spearhead of European and US players has come together and <a href="http://ea.newscpt4.de/_la.php?&nid=3412585&sid=196282272&lid=14453835&enc=687474703a2f2f65787465726e616c2e6f70656e67656f7370617469616c2e6f7267&tg=twiki_public/pub/CoveragesDWG/Datacubes/17-071_Datacube-DWG_Charter.pdf">suggested a working group charter</a>. Under the lead of rasdaman GmbH and Jacobs University, global players NASA, DigitalGlobe, European Union Satellite Centre, Envitia, and KEYW Corporation have established this charter which aims at establishing a cross-linked discussion forum at the widest possible level. While originally planned to publicly solicit comments ("public Request for Comments"), OGC has now put it on the <a href="http://ea.newscpt4.de/_la.php?&nid=3412585&sid=196282272&lid=14453840&enc=687474703a2f2f7777772e6f70656e67656f7370617469616c2e6f7267&tg=events/1712tcagenda">agenda</a> of the forthcoming Technical Committee meeting in Palmerston North, New Zealand beginning December. It can be expected that the public Request for Comments will be issued soon after.
</p><p>
The OGC Coverages working group will host the datacube session, given that the OGC coverage standards suite supports both regular and irregular n-D raster data "in a natural way", as Jordi Escriu, INSPIRE Thematic Cluster 3 Facilitator, puts it. Over the years and largely driven by the rasdaman team, OGC has adopted a rich <a href="http://ea.newscpt4.de/_la.php?&nid=3412585&sid=196282272&lid=14453845&enc=687474703a2f2f6d796f67632e6f7267&tg=go/coveragesDWG">coverage standards suite</a>, centered around the Coverage Implementation Schema (CIS) data model and the service model of Web Coverage Service (WCS) with its geo datacube analytics language, Web Coverage Processing Service (WCPS). Today, these standards are adopted by ISO and INSPIRE as well as a large, growing number of both open-source and proprietary tools on both client and server side. Today, operational installations utilizing the OGC Reference Implementation, rasdaman, exceed a Petabyte and thus underline the maturity of WCS/WCPS based services.
</p><p>
"When I started asking around it was not difficult to gather a critical mass of datacube enthusiasts from several continents", says Peter Baumann, Professor of Computer Science at Jacobs University and inventor of datacube query engines since 1992. "We are more than happy to see this massive interest in a field where we are innovating since decades, and to see a criticl-mass team-up happening".
</p>

