<a name="27-1"></a>
<h3>Risikoerfassung von Krankheiten auf dem Feld per App (in German)</h3>
<p><b>Erstmals haben Wissenschaftler mit BigData-Analysen Satellitenbilder und Wetterdaten kombiniert, um Frostschäden in Winterkulturen eindeutig zu identifizieren und Handlungsempfehlungen geben zu können. So lassen sich schnell mehrere hunderttausend Felder in ganz Deutschland untersuchen und per App Tipps für jeden einzelnen Schlag geben.
</b></p>
<p>
Pflanzenschutz, Düngung, Beregnung – ja oder nein, wieviel davon und wo genau? Das Verbundprojekt BigPicture hat hierzu Antworten gefunden. Satellitenbilder zeigen, wo auf dem Feld die Pflanzen gut oder schlecht wachsen. So sehen Landwirte mobil und unmittelbar, wo Handlungsbedarf besteht und können auf Teilflächen gezielt eingreifen.
</p>
<p>
„Wir wollen nicht nur wissen, dass die Pflanzen in einem Bereich des Feldes besser wachsen, sondern auch warum. Nur dann können wir Empfehlungen geben, welche Behandlung für die Teilfläche richtig ist,“ sagt Projektleiterin Dr. Katrin Kohler von der Spatial Business Integration GmbH in Darmstadt, die mit der Bremer rasdaman GmbH die Kompetenzen aus Satelliten- und BigData-Technologien vereint.
</p>
<p>
<b>Ganz Deutschland in einer halben Stunde analysiert:</b>
Viele Faktoren wirken auf das Pflanzenwachstum und müssen bei der Diagnose berücksichtigt werden: Bodeneigenschaften, gefolgt von Nährstoff- und Wasserversorgung sowie Krankheiten. Dementsprechend müssen die Eigenschaften jeder Teilfläche und die lokalen Wachstumsbedingungen anhand unterschiedlicher Daten untersucht werden. „So stellen wir fest, mit welcher Wahrscheinlichkeit eine Krankheit auf einer Teilfläche ausbrechen könnte“, erklärt Kohler.
</p>
<p>
In einem Fall kombinierte das Projektteam Satelliten- mit Wetterdaten, um Frostschäden oder Gelbrostrisiken in Winterkulturen eindeutig identifizieren zu können. So steigt das Risiko eines Gelbrostbefalls in Winterweizen mit zu- nehmender Dichte und feuchtem Mikroklima im Bestand. Dies kommt oft in Tallagen eines Feldes vor. Deshalb führten die Wissenschaftler entsprechende Informationen aus den Satellitenbildern und einem Digitalen Höhenmodell für die Risikoanalyse zusammen. „Eine solche Analyse, ausgeführt über hunderttausende von Feldern in ganz Deutschland, dauert nicht viel mehr als 30 Minuten,“ erklärt Prof. Peter Baumann von der Jacobs Universität Bremen, der die Daten mit Hilfe der hochskalierbaren und leistungsfähigen Datenwürfeltechnologie zusammenführt.
</p>
<p>
Den Landwirten sollen demnächst konkrete Handlungsempfehlungen für ihre Teilflächen zukommen. Dafür haben die Forscher bereits die App "Solorrow" entwickelt, die Landwirte kostenlos im App-Store herunterladen können. Diese stellt Landwirten bereits jetzt Satellitenbildkarten zur Verfügung, auf denen Teilflächen im Feld anhand ihres mehrjährigen Wachstumspotenzials unterschieden werden und eine angepasste Düngung unterstützen. In Zukunft soll diese um kombinierte Daten ergänzt werden. Das Bundeslandwirtschaftsministerium fördert das Verbundprojekt "BigPicture" noch bis August 2019 in seinem Programm zur Innovationsförderung über die Bundesanstalt für Landwirtschaft und Ernährung als Projektträger.
</p>
<p>
