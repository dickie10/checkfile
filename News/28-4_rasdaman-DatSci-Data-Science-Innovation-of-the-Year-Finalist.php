<h3>rasdaman: DatSci Data Science Innovation of the Year Finalist</h3>

<p>
<b>Dublin, September 2019 - The rasdaman datacube engine is finalist in the 2019 DatSci &amp; AI Awards. This prestigious European award recognizes rasdaman for its innovation, its advancement of data science in industry and business, and as such also its outstanding business impact.</b>
</p>
<p>
Data Science has arrived at the heart of industry and society as a key enabling combination of skills and technologies. Edward McDonnell, Director of CeADAR, The National Centre for Applied Data Analytics &amp; AI says, "The relentless pace and expansion of the analytics industry is reflected in the addition of new award categories covering AI, Industry 4.0, health and wellbeing. In fact, there are very few industries today that are not experimenting with or implementing analytics and AI solutions". Linda Davis, founder of the DatSci awards, adds that the European Commission released a report in 2017 that the amount of data workers in Europe will grow to 10.43 million by 2020.
</p>
<p>

The European DatSci &amp; AI Awards are driving data science forward by recognizing the best scientists, companies, and technologies in the field every year. "The DatSci &amp; AI Awards is on a mission to raise the profile of data science in Europe, especially with the exponential growth expected in the next two years," says Linda. "The awards in 2019 are the biggest to date, and the event itself will highlight the tremendous impact Europeâ€™s data scientists are making across all fields." The Awards recognise excellence in Data Science and Artificial Intelligence across industry, education and social responsibility in Europe in 10 Award Categories. This year, DatSci proudly cooperates with BDVA and CeADAR. The Big Data Value Association (BDVA) is the private counterpart to the EU Commission to implement the Big Data Value PPP program. CeADAR is the Irish National Centre for Applied Data Analytics &amp; AI. The 4th annual Awards Ceremony will take place on the 5th September 2019. The full-day event and Awards, hosted at Croke Park in Dublin, Ireland will connect and celebrate the best of the data science and artificial intelligence ecosystem across Europe. One winner will be awarded for each category and will be announced at the Awards on 5th September 2019.
</p>
<p>

The independent DatSci judges have honoured several aspects when selecting rasdaman: its contribution to analysis-ready Earth data by establishing datacubes; the scalable analytics capabilities of the rasdaman engine; and its strong impact on datacube standardization. "The team is happy and proud about this recognition, which stands in line with further international awards, like <a href="https://techconnectworld.com/World2019/">TechConnect</a> in the US recently", says Peter Baumann who is rasdaman architect, inventor of queryable datacubes, and initiator and editor of the datacube standards in OGC and ISO together with his team, with strong impacts into INSPIRE and other communities. "A highlight of our ongoing work 2019 certainly was the publication of the SQL datacube standard by ISO. We have been overwhelmed by the feedback of the ISO SQL experts when we proposed this extension back in 2014, and the joint effort now has led to a game changer in Data Science and AI", says Baumann.
</p>
