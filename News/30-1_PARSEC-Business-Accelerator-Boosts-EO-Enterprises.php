<h3>PARSEC Business Accelerator Boosts EO Enterprises</h3>
<p><b>
April 2020 -- 100 SMEs in the EO field have been selected for financial and technical support by the PARSEC business accelerator, 10 of them will receive the “grand prize”. Part of the support is the enabling multi-Petabyte PARSEC datacube platform which has been introduced to the SMEs this week. 
</b></p>
<p>
 Harnessing the power of Big EO Data requires efficient handling of the volume, velocity, variety, and veracity of multimodal EO data, such as the Copernicus Sentinels. SMEs entering the EO market require expensive experts top-skilled in IT and remote sensing alike, which distracts precious resources from the core business. These barriers make it difficult for SMEs gaining ground in the market. 
</p>
<p>
 This is where PARSEC comes to help. This European funded project will effectively support SMEs in addressing these challenges through both business development support, financial support, and a massively scalable EO datacube service. 
</p>
<p>
 PARSEC’s unique Big Data toolbox at its heart is powered by rasdaman, the European pioneer and world leading datacube analytics engine. With rasdaman’s federation capabilities, several DIASs as well as a series of further, often specialized data centers get integrated into one common information space available to all PARSEC supported SMEs. Currently, PARSEC has available over 7 Petabyte of Sentinel datacubes on Mundi alone, and they grow by the day. 
</p>
<p>
 A new era in data accessibility is dawning with spatio-temporal datacubes: orthoimagery, thematic and elevation data, weather and ocean data, and timeseries over them – they all get easily accessible when served analysis ready, thanks to open standards-based APIs for ingest, access, and analysis. The multi-parallel rasdaman engine supports a paradigm of “what you get is what you need” in real time on any query, anytime, and on any-size data. Both data and algorithms are protected by individually configurable security. 
</p>
<p>
 The rasdaman team, and the PARSEC team overall, will provide continuous support to the SMEs establishing their innovative EO product in shortest time. 
</p>
