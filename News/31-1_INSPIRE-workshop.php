<h3>INSPIRE Coverages Demystified</h3>
<p><b>
 Coverages appear across many INSPIRE themes in Annex II and III; specifically as raster data they represent a key part of any geographic data asset. Both WCS (with its geo datacube processing language, WCPS) and SOS are foreseen for serving them. However, from the Technical Guidance documents it is not immediately clear how to implement coverage services. Therefore, OGC, ISO, and INSPIRE experts have teamed up to establish best practices, evaluated and demonstrated through sample services. On June 10 at 11:00 CEST a <a href="http://ea.newscpt20.de/_la.php?&nid=4323590&sid=196282272&lid=18759980&enc=68747470733a2f2f72617364616d616e2e636f6d&tg=Events/INSPIRE-2020/">practical how-to</a> will be provided by a team of renowned experts as part of the virtual INSPIRE Conference. 
</b></p>
<p>
 Jordi Escriu (INSPIRE Facilitator Orthoimagery and Elevation), Kathi Schleidt (INSPIRE Facilitator Environmental Monitoring and Observations), and Peter Baumann (OGC CIS &amp; WCS editor) will introduce INSPIRE coverage handling and explain step by step how coverages can be modelled with fully INSPIRE-compliant metadata while still compliant with the OGC coverage standard, CIS. Further, we will demonstrate how WCS / WCPS and SOS services can be established on such coverages. Practical examples will make use of publicly accessible WCS services powered by the rasdaman reference implementation and the DataCove SOS. Participants can replay and modify many of these examples. Also discussed will be the current coverage status across OGC, ISO, and INSPIRE, including the forthcoming OGC OAPI-Coverages extension to WCS. 
</p>
<p>
 Altogether, this workshop constitutes a unique opportunity for getting up to speed with INSPIRE coverage data and services, and to bring along questions for clarification. 
</p>

