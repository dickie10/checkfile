<h3><a href="https://inspire-wcs.de/events/2020-10-13_INSPIRE_webinar-coverages/">Jetzt registrieren! INTERGEO INSPIRE Coverages Webinare</a></h3>
<p><b>
Nach wie vor sind nur wenige Raster- bzw. Coverage-Daten und -Dienste in INSPIRE konformer Weise verfügbar, obwohl es durchaus detaillierte Anleitungen dazu gibt. Auf der diesjährigen virtuellen Intergeo werden zu diesem Thema zwei offene, kostenfreie Online-Webinare angeboten. Das erste findet am 13. Oktober statt und bietet einen Überblick über die relevanten Standards. Das zweite, am 14. Oktober, geht auf technische Details ein. Beide Webinare sind interaktiv geplant. Fragestellungen der Teilnehmer sind willkommen.
</b></p>
<p>
Immer noch sind Dienste auf Rasterdaten nicht immer einfach aufzusetzen, insbesondere wenn die INSPIRE-Vorgaben eingehalten werden sollen. Das liegt nicht etwa an Restriktionen, sondern an Unsicherheiten und mangelnder Information über den optimalen Einsatz der Coverage-Standards. In den Webinaren erhalten Interessierte eine umfassende Einführung in die INSPIRE Coverage Anwendungen. Ein Team aus Coverage- und INSPIRE Experten erklärt Schritt für Schritt, wie die INSPIRE konforme Modellierung von Coverages, inklusive der Verknüpfung von Metadaten, funktioniert. Datenzugriff, Verteilung, Visualisierung und Datenfusion von Coverages durch WCS/WCPS Dienste kann sehr einfach sein. Praktische Beispiele zeigen, wie man öffentlich zugängliche WCS Dienste nutzt, die von der Referenzimplementierung rasdaman unterstützt werden. Die Teilnehmer können viele dieser Beispiele wiederholen und für Ihre Zwecke modifizieren. Für die Präsentation und Diskussion zum derzeitigen Standardisierungs-Status in OGC, ISO und INSPIRE ist reichlich Zeit vorhanden. Auch die in Entwicklung befindliche OGC OAPI Coverage-Erweiterung zum WCS wird vorgestellt.
</p>
<p>
Rundherum ist dieser Workshop eine wertvolle Gelegenheit, die INSPIRE Coverage-Daten und -Dienste kennenzulernen und Fragen zu klären. Eine frühe Anmeldung ist sinnvoll, denn die Anzahl der Plätze ist begrenzt.
</p>
