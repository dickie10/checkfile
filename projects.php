                <h4><a href="http://www.earthserver.eu">EarthServer</a>: open, flexible, scalable, and user-friendly access to spatio-temporal Earth science data</h4>
                <p>
                </p>
                <p>
EarthServer has established services and technology for open, flexible,
scalable, and user-friendly access to spatio-temporal Earth science data. Accessing such
data ranges from simple navigation over extraction, download, filtering, and processing up to
complex analytics. This is accomplished through versatile ad-hoc analytics suitable for
extreme-size multi-dimensional Earth Science data, based on and extending leading-edge
Array Database technology.
                </p>
                <p>
EarthServer combines both, thereby achieving tight data/metadata integration. Further, the
rasdaman Array Database System is extended with further spatiotemporal coverage data
types. On server side, highly effective optimizations -- such as parallel and distributed query
processing -- ensure scalability to Exabyte volumes.
                </p>
                <p>
Several Lighthouse Applications, spanning all Earth sciences, have been established
with up to 132 TB databases covering: atmosphere (climate modelling), hydrosphere
(oceanography), lithosphere (geology), and cryosphere (snow and ice mapping); in addition, a
air-borne science (drone) imagery database has been set up and a Planetary science use case
with surface and subsurface data on Mars.
                </p>
                <p>
The main innovations of EarthServer are:
                </p>
                <ul>
                  <li>Substantial simplification of use of any-size, any-dimension Big Earth Data, through flexible, scalable, integrated, standards-based access interfaces;
                  <li>Integrating data and metadata in Big Earth Data queries,
                  <li>Uniform access across space and time, enabling large image and weather timeseries;
                  <li>Web service support for across all OGC coverage types: multi-dimensional grids, point clouds, and meshes;
                  <li>Transparent queries across heterogeneous file archives and databases;
                  <li>Paving the way for Petabyte Copernicus and climate services through parallel and distributed processing of queries.
                  <li>Strict reliance on the open OGC standards for Big Earth Data access, Web Coverage Service (WCS) and Web Coverage Processing Service (WCPS), including advancing these standards themselves.
                </ul>
                <p>
Nucleus is the pre-existing rasdaman system which has coined a new research field, Array
Databases. During EarthServer, rasdaman has strengthened its position as the world‟s leading
Array Database over recent competitors such as SciDB. This substantial technological
advance has resulted in a series of awards for Jacobs Uni and rasdaman GmbH, such as
winner of the Big Data challenge in the Copernicus Masters competition 2014.
                </p>
                <p>
A wide range of outreach activities has accompanied technology development and service de-
ployment. Trainings, best practices, and auxiliary software tools have been provided at events
and on the Web. A multitude of communication channels have been used to position the
EarthServer technology worldwide (such as GEO Plenaries, Copernicus workshops, cli- mate
data workshops, standardization meetings). A particular highlight is a 1h TV documentary
titled “Big Earth Data – the digitized planet” (Fig. 3) done by Jacobs and rasdaman GmbH
one in collaboration with German ZDF and ARTE, to be broadcast soon after the project.
While focusing on Earth sciences, this project transcends Earth and Planetary data; rather,
Array Databases comprise horizontal platform technology equally game-changing Life
sciences, but also industry (such as oil and gas exploration) and eGovernment. In the latter
context, during EarthServer INSPIRE has agree to base its Download Services for coverages
on the OGC Web Coverage Service (WCS); work commenced in October 2014. To foster
such uptake, industry and authorities have been involved in EarthServer.
                </p>
