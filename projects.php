            <div class="col-md-10">
                <h4>ORBiDANSe: Orbital Big Data Analytics Service</h4>

                <p>
                  This project drives the "ship code to data" paradigm to the extreme:
                  it makes a Cubesat an online Web data service for real time EO acquisition,
                  processing, and retrieval, based on the ISO Array SQL standard under adoption.
                  Images get acquired by the on-board camera and geo-referenced via GPS.
                  Access is done via a high-level array query language allowing ad-hoc processing
                  and filtering on spatio-temporal raster data, similar to what standard SQL
                  accomplishes on tuple sets.
                </p>

                <p>
                  On board, such queries are evaluated by the rasdaman Big Array Data Analytics engine.
                  Among others, it supports spatio-temporal queries, hence is truly multi-dimensional.
                  The configuration can be updated/reconfigured in-flight, although emphasis is
                  put on automatic optimization, including acquisition planning based on incoming queries.
                  In a direct scenario, targeted subsetting/pocessing of imagery can be downlinked directly
                  to the requesting client, effectively turning the satellite into an image database.
                  In a federated scenario, a client may submit some complex decision support query to a data center;
                  the rasdaman instance there finds out that data are missing and spawns a sub-request to the Cubesat;
                  merges its locally computed results with the Cubesat response into the final result sent back to the user.
                  As rasdaman is already cloud-parallelized, queries can be distributed automatically
                  between ground and space instances.
                </p>

                <p>
                  Overall goal is to achieve a quantum leap in both EO service quality, data availability, and service integration.
                </p>

                <p>
                  This project has been supported by German BMBF (Ministry of Education and Research).
                </p>
