                <h4>DIN-CONNECT: Establishing the Next-Generation ISO Coverage Standard</h4>
                <p>
German standardization body, DIN, <a href="https://www.din.de/en/wdc-beuth:din21:288317861">has tasked rasdaman GmbH</a> with establishing the next-generation high-level coverage standard, ISO 19123-1 Abstract Coverage Model. Based on the fact that the company already has established the OGC Coverage Implementation Schema, which meantime is adopted by ISO as 19123-2, DIN relies on the unmatched experience of rasdaman GmbH when it comes to coverages and datacubes.
